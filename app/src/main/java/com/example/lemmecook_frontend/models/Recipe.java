package com.example.lemmecook_frontend.models;

public class Recipe {
    int id;
    String title;
    String image;
    String imageType;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getImage() {
        return image;
    }

    public String getImageType() {
        return imageType;
    }
}